console.log("Hola Mundo");
//Consultar funciones de console
console.error("Error") //Muestra un mensaje de error en la Consola Web.
console.dir ()//Muestra el elemento en un arbol JSON
console.time() // Inicia un temporizador que puede usar para realizar un seguimiento de la duración de una operación
console.assert() // Aparece un mensaje de error en la consola si la afirmacion es falsa. Si la afirmacion es verdadera no aparecera nada 
console.dirxml() //Muestra en consola un arbol interactivo de los elementos descendientes del elemento especificado.
console.table() // muestra data con una tabla en la consola cada elemento en el array (o propiedad enumerable si data es un objeto )sera una fila en la tabla 
console.timeEnd() //El nombre del temporizador a detener. Al detenerse, el tiempo transcurrido será mostrado automáticamente en la Consola del Navegador.
console.trace() // muestra un rastro que muestra cómo terminó el código en un punto determinado.
console.warn() //Una lista de objetos JavaScript para ser emitidos en la Consola Web. Las representaciones como string de cada uno de estos objetos son anexados juntos en el orden listado e impresos.
console.group() // Puede utilizar grupos anidados para ayudar a organizar su salida asociando visualmente mensajes relacionados.
console.groupCollapsed() //  indica el inicio de un grupo de mensajes contraído.
console.countReset() // restablece el recuento de esa etiqueta a 0. Si se omite, countReset()restablece el contador predeterminado a 0.
console.clear() // método borra la consola si el entorno lo permite.
